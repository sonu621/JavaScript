// ------------ Switch in JS-----------------

const month = 3;

switch (month) {
  case 1:
    console.log("January");
    break;
  case 2:
    console.log("Febuary");
    break;
  case 3:
    console.log("March");
    break;
  default:
    console.log("Default case match");
    break;
}

const name = "Sonu";

switch (name) {
  case "Sapna":
    console.log("Sapna Sharma");
    break;
  case "Sonu":
    console.log("Sonu Gupta");
}
